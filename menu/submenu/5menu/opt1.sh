#!/bin/bash
submenu::5opt1() {
  cat <<EOF
This module is used to calculate band alignment.
Band alignment is an important parameter in the study of semiconductor heterojunctions,
and it is directly related to many properties of the material, so it is of great significance to
accurately measure or calculate this value. Experimentally, band alignment can be measured by
photoelectron spectroscopy and optical methods. However, because the measured value has a great
relationship with the specific growth environment, such as the magnitude of the lattice strain,
the test results of different measurers will vary greatly and cannot be directly compared. So,
theoretically calculating the band alignment of semiconductors is very important.（算能带带阶）
EOF

exit 0
}
